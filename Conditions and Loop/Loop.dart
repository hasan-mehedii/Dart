void main() {

  // For Loop
  for (int i = 0; i < 10; i++) {
    print("John Doe");
  }

  while (i <= 10) {
    print(i);
    i++;
  }

  int i = 1;
  do {
    print(i);
    i++;
  } while (i <= 10);
}

